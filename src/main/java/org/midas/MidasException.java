package org.midas;

public class MidasException extends Exception
{
	public MidasException()
	{
		super();
	}

	public MidasException(String arg0, Throwable arg1)
	{
		super(arg0, arg1);
	}

	public MidasException(String arg0)
	{
		super(arg0);
	}

	public MidasException(Throwable arg0)
	{
		super(arg0);
	}
}
